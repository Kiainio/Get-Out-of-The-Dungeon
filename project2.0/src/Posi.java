class Posi {
    private int Line;
    private int Row;

    Posi(int row, int line) {
        this.Line = line;
        this.Row = row;
    }

    int getLine() {
        return Line;
    }

    void setLine(int line) {
        Line = line;
    }

    int getRow() {
        return Row;
    }

    void setRow(int row) {
        Row = row;
    }
}
